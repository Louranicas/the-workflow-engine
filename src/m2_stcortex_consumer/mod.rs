#[cfg(test)]
mod tests {
    use super::{
        consumption_event_query, tool_call_query, ConsumerIdentity, ConsumerName, Namespace,
        StcortexConsumerError, Transport, WORKFLOW_TRACE_PREFIX,
    };

    #[test]
    fn reexports_prefix_constant() {
        // Re-exported from m9's single source of truth.
        assert_eq!(
            WORKFLOW_TRACE_PREFIX,
            crate::m9_watcher_namespace_guard::WORKFLOW_TRACE_NS_PREFIX
        );
    }

    #[test]
    fn reexports_consumer_name_validator() {
        assert!(ConsumerName::new("workflow-trace-abc").is_ok());
        assert!(matches!(
            ConsumerName::new(""),
            Err(StcortexConsumerError::InvalidConsumerName(_))
        ));
    }

    #[test]
    fn reexports_namespace_validator() {
        assert!(Namespace::new("workflow_trace_x").is_ok());
        assert!(matches!(
            Namespace::new("orac_foo"),
            Err(StcortexConsumerError::InvalidNamespace(_))
        ));
    }

    #[test]
    fn reexports_transport_subscription_only() {
        assert_eq!(Transport::Subscription.as_str(), "subscription");
    }

    #[test]
    fn reexports_query_builders() {
        let q = tool_call_query(WORKFLOW_TRACE_PREFIX);
        assert!(q.contains("LIKE"));
        assert!(q.contains(WORKFLOW_TRACE_PREFIX));
        assert_eq!(consumption_event_query(), "SELECT * FROM consumption_event");
    }

    #[test]
    fn from_git_sha_uses_workflow_trace_prefix() {
        let ns = Namespace::new("workflow_trace_test").unwrap();
        let id = ConsumerIdentity::from_git_sha(ns);
        assert!(id.name.as_str().starts_with("workflow-trace-"));
    }
}
