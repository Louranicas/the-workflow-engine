//! `m2_stcortex_consumer` — workflow-trace's narrowed-scope consumer for
//! stcortex (SpacetimeDB at `ws://127.0.0.1:3000`).
//!
//! See [m2 spec](../../../ai_specs/modules/cluster-D/m2_stcortex_consumer.md).
//!
//! # Day-1 surface
//!
//! - [`ConsumerName`] / [`Namespace`] / [`Transport`] / [`ConsumerIdentity`]
//!   newtypes — AP30 enforcement lives in the type system.
//! - [`tool_call_query`] / [`consumption_event_query`] — the **only two**
//!   narrowed subscription queries. W1 invariant: subscription strings
//!   are hardcoded; widening to `pathway` / `memory` requires a v1.4
//!   spec amendment (and re-evaluation of F8 Watcher feedback-loop
//!   poisoning).
//! - [`register_narrowed_consumer`] — synchronous entry point. Builds a
//!   `DbConnection`, invokes `register_consumer`, subscribes, waits for
//!   `on_applied`.
//! - [`RegistrationHandle::is_fresh`] — the CC-2 trust-signal gate that
//!   m13 reads before every write attempt.
//! - [`StcortexRow`] / [`ToolCallRow`] / [`ConsumptionEventRow`] — typed
//!   delta row mirrors (callback payloads).
//!
//! # Vendored module_bindings
//!
//! `module_bindings/` is a verbatim copy of
//! `stcortex/clients/rust-subscriber/src/module_bindings/` (generated by
//! the SpacetimeDB CLI). It mirrors the live stcortex schema as of the
//! copy date. Regenerate on schema change. The submodule is private to
//! this crate.

